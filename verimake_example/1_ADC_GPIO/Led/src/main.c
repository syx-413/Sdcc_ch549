#include <CH549_sdcc.H>	 //ch549的头文件，其中定义了单片机的一些特殊功能寄存器
    
#define led P2_2  //将单片机的P2.2端口定义为led

/********************************************************************
* 函 数 名       : delay
* 函数功能		   : 延时函数
* 输    入       : 时间
* 输    出    	 : 无
********************************************************************/
void delay(int i)
{
	while(i--);	
}

/********************************************************************
* 函 数 名       : main
* 函数功能		 : 主函数
* 输    入       : 无
* 输    出    	 : 无
*********************************************************************/
void main()
{ 
   while(1)
	 {
   led =!led;    //p2.2输出取反
	 delay(50000);
	 }
//代码会在while（1）中无限循环
} 